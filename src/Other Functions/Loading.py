def carga():
    """
    (⌐■_■):
    """
    import time

    def animation(iteraciones=8, retraso=0.5):
        """
        (⌐■_■):
        """
        for _ in range(iteraciones):
            print("-", end="-", flush=True)
            time.sleep(retraso)

    animation()

def main():
    print("Loading")
    carga()
    print("\n¡Carga completa")

if __name__ == "__main__":
    main()